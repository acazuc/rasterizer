#include "vec4.h"

void	vec4_add(t_vec4 *dst, t_vec4 *v1, t_vec4 *v2)
{
	dst->x = v1->x + v2->x;
	dst->y = v1->y + v2->y;
	dst->z = v1->z + v2->z;
	dst->w = v1->w + v2->w;
	dst->u = v1->u + v2->u;
	dst->v = v1->v + v2->v;
	color_add(&dst->color, &v1->color, &v2->color);
}
